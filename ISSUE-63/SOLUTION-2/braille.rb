ALPHABETH = Hash.new {|hash, x|
  if x =~ /^[0-9]+$/
    x.split(//).reduce(['*.', '*.', '..']) {|x, y|
      x.zip(hash[y]).map{|x|
        x.join(' ')
      }
    }
  elsif x =~ /[A-ZÀÈÉÌÒÙ]/
    ['*.', '**', '*.'].zip(hash[x.downcase]).map{|x|
      x.join(' ')
    }
  end
}.update({
  ' ' => ['  ', '  ', '  '],
  ?a => ['.*', '**', '**'],
  ?1 => ['.*', '**', '**'],
  ?b => ['.*', '.*', '**'],
  ?2 => ['.*', '.*', '**'],
  ?c => ['..', '**', '**'],
  ?3 => ['..', '**', '**'],
  ?d => ['..', '*.', '**'],
  ?4 => ['..', '*.', '**'],
  ?e => ['.*', '*.', '**'],
  ?5 => ['.*', '*.', '**'],
  ?f => ['..', '.*', '**'],
  ?6 => ['..', '.*', '**'],
  ?g => ['..', '..', '**'],
  ?7 => ['..', '..', '**'],
  ?h => ['.*', '..', '**'],
  ?8 => ['.*', '..', '**'],
  ?i => ['*.', '.*', '**'],
  ?9 => ['*.', '.*', '**'],
  ?j => ['*.', '..', '**'],
  ?0 => ['*.', '..', '**'],
  ?k => ['.*', '**', '.*'],
  ?l => ['.*', '.*', '.*'],
  ?m => ['..', '**', '.*'],
  ?n => ['..', '*.', '.*'],
  ?o => ['.*', '*.', '.*'],
  ?p => ['..', '.*', '.*'],
  ?q => ['..', '..', '.*'],
  ?r => ['.*', '..', '.*'],
  ?s => ['*.', '.*', '.*'],
  ?t => ['*.', '..', '.*'],
  ?u => ['.*', '**', '..'],
  ?v => ['.*', '.*', '..'],
  ?w => ['*.', '..', '*.'],
  ?x => ['..', '**', '..'],
  ?y => ['..', '*.', '..'],
  ?z => ['.*', '*.', '..'],
  ?. => ['**', '..', '*.'],
  ?, => ['**', '.*', '**'],
  ?? => ['**', '.*', '*.'],
  ?; => ['**', '.*', '.*'],
  ?! => ['**', '..', '.*'],
  ?( => ['**', '..', '..'],
  ?) => ['**', '..', '..'],
  ?\n => ['**', '**', '..'],
  ?à => ['.*', '..', '..'],
  ?è => ['..', '..', '..'],
  ?é => ['..', '..', '..'],
  ?ì => ['*.', '**', '.*'],
  ?ò => ['*.', '**', '..'],
  ?ù => ['*.', '..', '..']
})

puts ARGV[0].scan(/[a-z.,?;!"()\nàèéìòù ]|[0-9]+/i).reduce(["", "", ""]) {|x, y|
  x.zip(ALPHABETH[y]).map {|x|
    x.join(' ')
  }
}.join("\n")
